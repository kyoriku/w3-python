# Write a Python program that uses a while loop to print numbers from 1 to 5 and then prints "i is no longer less than 6" when the loop exits.

# Requirements
# The loop should print numbers 1 through 5 inclusive.
# The program should print 'i is no longer less than 6' after the loop exits.

i = 1
while i < 6:
  # Modify here
  print(i)
  i += 1
else:
  # Print the message when the loop exits
  print("i is no longer less than 6")