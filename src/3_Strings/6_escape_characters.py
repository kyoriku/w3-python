# Double Quote
txt = "We are the so-called \"Vikings\" from the north."
print(txt) 

# Single Quote
txt = 'It\'s alright.'
print(txt) 

# Backslash
txt = "This will insert one \\ (backslash)."
print(txt) 

# New Line
txt = "Hello\nWorld!"
print(txt) 

# Carriage Return
txt = "Hello\rWorld!"
print(txt) 

# Tab
txt = "Hello\tWorld!"
print(txt) 

# Backspace
txt = "Hello \bWorld!"
print(txt) 

# Form feed
txt = "Hello\fWorld!"
print(txt) 

# Octal value
txt = "\110\145\154\154\157"
print(txt) 

# Hex value
txt = "\x48\x65\x6c\x6c\x6f"
print(txt) 