# Slicing
b = "Hello, World!"
print(b[2:5])  # characters from position 2 to position 5 (not included)

# Slice From the Start
b = "Hello, World!"
print(b[:5])  # characters from the beginning to position 5 (not included)

# Slice To the End
b = "Hello, World!"
print(b[2:])  # characters from position 2 to the end

# Negative Indexing
b = "Hello, World!"
print(b[-5:-2])  # characters from position -5 to -2 (not included)