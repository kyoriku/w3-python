# Upper Case
a = "Hello, World!"
print(a.upper())  # converts all characters to upper case

# Lower Case
a = "Hello, World!"
print(a.lower())  # converts all characters to lower case

# Remove Whitespace
a = " Hello, World! "
print(a.strip())  # removes any whitespace from the beginning and end

# Replace String
a = "Hello, World!"
print(a.replace("H", "J"))

# Split String
a = "Hello, World!"
print(a.split(","))  # splits the string into a list